#include <iostream>
using namespace std;

int main(){
  int a,b;
  cout << "Contoh Program Perulangan "<<endl;
  for (a=1; a<=10; a++){
    cout << a << " ";
    cout << endl;
  }
  cout << "  " << endl;
  cout << "Perulangan Menurun " << endl;
  for (b=10; b>=1; b--){
    cout << b << " ";
    cout << endl;
  }
  return 0;
}